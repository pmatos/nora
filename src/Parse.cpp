// Source range covering a single token [Start, End).
llvm::SMRange tokRange(const SourceStream &S, const Tok &T) {
  return {S.getLoc(T.Start), S.getLoc(T.End + 1)};
}

// Source range from a node's first-token offset to the current parse position.
llvm::SMRange rangeFrom(const SourceStream &S, size_t NodeStart) {
  return {S.getLoc(NodeStart), S.getLoc(S.getPosition())};
}

// Report a parse error anchored at a byte offset, if diagnostics are enabled.
void parseError(SourceStream &S, size_t Offset, const llvm::Twine &Msg) {
  if (auto *Diag = S.getDiagnostics()) {
    Diag->error(S.getLoc(Offset), Msg);
  }
}

// True once a diagnostic has been emitted, so backtracking can stop trying
// alternatives instead of cascading further errors.
bool hadError(const SourceStream &S) {
  auto *Diag = S.getDiagnostics();
  return Diag != nullptr && Diag->hadError();
}

